public class SumOfNumbers {
    public static void main(String[] args) {
        int sum = 0;

        // Loop through the first 10 numbers (1 to 10)
        for (int i = 1; i <= 10; i++) {
            sum += i;  // Add i to sum
        }

        // Print the sum
        System.out.println("The sum of the first 10 numbers is: " + sum);
    }
}
